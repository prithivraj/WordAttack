package com.extremeboredom.wordattack.utils;

/**
 * Singleton helper class for lazily initialization.
 *
 * @author jingle1267@163.com
 * @date 2014-9-11
 */
public abstract class Singleton<T> {
    private T mInstance;

    protected abstract T create();

    public final T get() {
        synchronized (this) {
            if (mInstance == null) {
                mInstance = create();
            }
            return mInstance;
        }
    }
}
